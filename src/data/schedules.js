function parseScheduleCsv(csv) {
  const rows = csv.trim().split(/\r?\n/).slice(1);

  return rows
    .map((row) => row.split(','))
    .filter((columns) => columns.length >= 9)
    .map((columns) => ({
      nr: Number(columns[0]),
      time: columns[1],
      planNr: Number(columns[2]),
      plan: columns[3],
      group: columns[5],
      type: columns[6],
      team1: columns[7],
      team2: columns[8],
      result: columns[9] || '',
    }));
}

